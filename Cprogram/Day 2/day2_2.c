#include <stdio.h>
int main()
{
    char ch1='A',ch2='Z',ch3='a',ch4='z';
    printf("ASCII value of 'A' is %d\n",ch1);
    printf("ASCII value of 'Z' is %d\n",ch2);
    printf("ASCII value of 'a' is %d\n",ch3);
    printf("ASCII value of 'z' is %d\n",ch4);

    printf("ASCII value of '0' is %d\n",'0');
    printf("ASCII value of '\\n' is %d\n",'\n');

}