#include<stdio.h>
int main()
{
    int num1=25,num2=50;
    printf("<: num1<num2 = %d\n",num1<num2);
    printf(">: num1>num2 = %d\n",num1>num2);
    printf("<=: num1<=num2 = %d\n",num1<=num2);
    printf(">=: num1>=num2 = %d\n",num1>=num2);
    printf("==: num1==num2 = %d\n",num1==num2);
    printf("!=: num1!=num2 = %d\n",num1!=num2);

}